using CSSPCultureServices.Services;
using CSSPEnums;
using GenerateCodeBaseHelper;
using GenerateCodeBaseServices.Models;
using Microsoft.AspNetCore.Mvc;
using Microsoft.Extensions.Configuration;
using System;
using System.Collections.Generic;
using System.Data;
using System.Globalization;
using System.IO;
using System.Linq;
using System.Text;
using System.Text.RegularExpressions;
using System.Threading.Tasks;

namespace GenerateAngularEnums
{
    public partial class Startup
    {
        public async Task<bool> Generate()
        {
            Console.WriteLine("Generate Starting ...");

            DirectoryInfo diOutputGen = new DirectoryInfo(Configuration.GetValue<string>("OutputDir"));
            if (!diOutputGen.Exists)
            {
                try
                {
                    diOutputGen.Create();
                }
                catch (Exception)
                {
                    Console.WriteLine($"Could not create directory { diOutputGen.FullName }");
                    return false;
                }
            }

            FileInfo fiCSSPEnumsDLL = new FileInfo(Configuration.GetValue<string>("CSSPEnums"));

            Console.WriteLine($"Reading [{ fiCSSPEnumsDLL.FullName }] ...");

            List<DLLTypeInfo> DLLTypeInfoCSSPEnumsList = new List<DLLTypeInfo>();
            if (GenerateCodeBase.FillDLLTypeInfoList(fiCSSPEnumsDLL, DLLTypeInfoCSSPEnumsList))
            {
                Console.WriteLine($"Could not read file { diOutputGen.FullName }");
                return false;
            }
            Console.WriteLine($"Loaded [{ fiCSSPEnumsDLL.FullName }] ...");

            foreach (DLLTypeInfo dllTypeInfoEnums in DLLTypeInfoCSSPEnumsList)
            {
                //if (dllTypeInfoEnums.Name != "AddressTypeEnum")
                //{
                //    continue;
                //}

                StringBuilder sb = new StringBuilder();

                if (dllTypeInfoEnums.IsEnum)
                {
                    sb.AppendLine(@"/*");
                    sb.AppendLine($@" * Auto generated { AppDomain.CurrentDomain.BaseDirectory }{ AppDomain.CurrentDomain.FriendlyName}.exe");
                    sb.AppendLine(@" *");
                    sb.AppendLine(@" * Do not edit this file.");
                    sb.AppendLine(@" *");
                    sb.AppendLine(@" */");
                    sb.AppendLine(@"");
                    sb.AppendLine(@"import { AppLanguageService } from 'src/app/services/app/app-language.service';");
                    sb.AppendLine(@"import { EnumIDAndText } from 'src/app/models/generated/models/EnumIDAndText.model';");
                    if (dllTypeInfoEnums.Name != "LanguageEnum")
                    {
                        sb.AppendLine(@"import { LanguageEnum } from 'src/app/enums/generated/LanguageEnum';");
                    }
                    sb.AppendLine(@"");

                    sb.AppendLine($@"export enum { dllTypeInfoEnums.Name } {{");

                    List<EnumNameAndNumber> enumNameAndNumberList = new List<EnumNameAndNumber>();

                    foreach (DLLFieldInfo dllFieldInfo in dllTypeInfoEnums.FieldInfoList)
                    {
                        if (dllTypeInfoEnums.IsEnum)
                        {

                            string fName = dllFieldInfo.Name;
                            int IntVal = (int)dllFieldInfo.FieldInfo.GetValue(dllFieldInfo.Name);

                            enumNameAndNumberList.Add(new EnumNameAndNumber() { EnumName = fName, EnumNumber = IntVal });
                        }
                    }

                    foreach (EnumNameAndNumber enumNameAndNumber in enumNameAndNumberList.OrderBy(c => c.EnumNumber))
                    {
                        sb.AppendLine($@"    { enumNameAndNumber.EnumName } = { enumNameAndNumber.EnumNumber },");
                    }

                    sb.AppendLine(@"}");
                    sb.AppendLine(@"");

                    // ---------------------------------
                    // doing Get{EnumName}()
                    // ---------------------------------
                    sb.AppendLine($@"export function Get{ dllTypeInfoEnums.Name }(): typeof { dllTypeInfoEnums.Name }");
                    sb.AppendLine(@"{");
                    sb.AppendLine($@"  return { dllTypeInfoEnums.Name };");
                    sb.AppendLine(@"}");
                    sb.AppendLine(@"");


                    // ---------------------------------
                    // doing {EnumName}_GetOrderedText()
                    // ---------------------------------
                    sb.AppendLine($@"export function { dllTypeInfoEnums.Name }_GetOrderedText(appLanguageService: AppLanguageService): EnumIDAndText[] {{");
                    sb.AppendLine(@"    let enumTextOrderedList: EnumIDAndText[] = [];");
                    sb.AppendLine(@"    if (appLanguageService.Language == LanguageEnum.fr) {");

                    CSSPCultureService.SetCulture("fr-CA");
                    List<EnumIDAndText> enumIDAndTextList = Enums.GetEnumTextOrderedList(dllTypeInfoEnums.Type);
                    foreach (EnumIDAndText enumIDAndText in enumIDAndTextList.OrderBy(c => c.EnumID))
                    {
                        string EnumText = enumIDAndText.EnumText.Replace("'", "\\'").Replace(Convert.ToChar(160), ' ');

                        RegexOptions options = RegexOptions.None;
                        Regex regex = new Regex("[ ]{2,}", options);
                        EnumText = regex.Replace(EnumText, " ").Trim();

                        sb.AppendLine($@"        enumTextOrderedList.push({{ EnumID: { enumIDAndText.EnumID }, EnumText: '{ EnumText }' }});");
                    }

                    sb.AppendLine(@"    }");
                    sb.AppendLine(@"    else {");

                    CSSPCultureService.SetCulture("en-CA");
                    enumIDAndTextList = Enums.GetEnumTextOrderedList(dllTypeInfoEnums.Type);
                    foreach (EnumIDAndText enumIDAndText in enumIDAndTextList.OrderBy(c => c.EnumID))
                    {
                        string EnumText = enumIDAndText.EnumText.Replace("'", "\\'").Replace(Convert.ToChar(160), ' ');

                        RegexOptions options = RegexOptions.None;
                        Regex regex = new Regex("[ ]{2,}", options);
                        EnumText = regex.Replace(EnumText, " ").Trim();

                        sb.AppendLine($@"        enumTextOrderedList.push({{ EnumID: { enumIDAndText.EnumID }, EnumText: '{ EnumText }' }});");
                    }

                    sb.AppendLine(@"    }");
                    sb.AppendLine(@"");
                    sb.AppendLine(@"    return enumTextOrderedList.sort((a, b) => a.EnumText.localeCompare(b.EnumText));");
                    sb.AppendLine(@"}");

                    // ---------------------------------
                    // doing {EnumName}_GetIDText(enumID: number)
                    // ---------------------------------
                    sb.AppendLine(@"");
                    sb.AppendLine($@"export function { dllTypeInfoEnums.Name }_GetIDText(enumID: number, appLanguageService: AppLanguageService): string {{");
                    sb.AppendLine($@"    let { dllTypeInfoEnums.Name }Text: string;");
                    sb.AppendLine($@"    { dllTypeInfoEnums.Name }_GetOrderedText(appLanguageService).forEach(e => {{");
                    sb.AppendLine($@"        if (e.EnumID == enumID) {{");
                    sb.AppendLine($@"            { dllTypeInfoEnums.Name }Text = e.EnumText;");
                    sb.AppendLine($@"            return false;");
                    sb.AppendLine(@"        }");
                    sb.AppendLine(@"    });");
                    sb.AppendLine(@"");
                    sb.AppendLine($@"    return { dllTypeInfoEnums.Name }Text;");
                    sb.AppendLine(@"}");

                    FileInfo fiOutputGen = new FileInfo(Configuration.GetValue<string>("EnumNameFile").Replace("{EnumName}", dllTypeInfoEnums.Name.Replace("enum", ".enum")));

                    using (StreamWriter sw2 = fiOutputGen.CreateText())
                    {
                        sw2.Write(sb.ToString());
                        Console.WriteLine($"Created [{ fiOutputGen.FullName }]");
                    }
                }
            }

            Console.WriteLine("");
            Console.WriteLine($"Done ...");
            Console.WriteLine("");
            Console.WriteLine("Generate Finished ...");

            return await Task.FromResult(true);
        }
    }
}
