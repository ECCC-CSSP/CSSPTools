namespace CreateGzFileServices
{
    public partial class CreateGzFileService : ControllerBase, ICreateGzFileService
    {
        private async Task<ActionResult<bool>> DoCreateWebAllSearchGzFile()
        {
            if (LoggedInService.LoggedInContactInfo.LoggedInContact == null)
            {
                return await Task.FromResult(Unauthorized(CSSPCultureServicesRes.YouDoNotHaveAuthorization));
            }

            WebAllSearch WebAllSearch = new WebAllSearch();

            try
            {
                await FillAllSearchTVItemModelList(WebAllSearch.TVItemModelList);

                if (dbLocal != null)
                {
                    await DoStoreLocal<WebAllSearch>(WebAllSearch, $"{ WebTypeEnum.WebAllSearch }.gz");
                }
                else
                {
                    await DoStore<WebAllSearch>(WebAllSearch, $"{ WebTypeEnum.WebAllSearch }.gz");
                }
            }
            catch (Exception ex)
            {
                string inner = ex.InnerException != null ? $"Inner: { ex.InnerException.Message }" : "";
                return await Task.FromResult(BadRequest($"{ ex.Message } { inner }"));
            }

            return await Task.FromResult(Ok(true));
        }
    }
}