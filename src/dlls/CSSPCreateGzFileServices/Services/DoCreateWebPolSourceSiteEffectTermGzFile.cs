using CSSPCultureServices.Resources;
using CSSPEnums;
using CSSPModels;
using Microsoft.AspNetCore.Mvc;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;

namespace CreateGzFileServices
{
    public partial class CreateGzFileService : ControllerBase, ICreateGzFileService
    {
        private async Task<ActionResult<bool>> DoCreateWebPolSourceSiteEffectTermGzFile()
        {
            if (LoggedInService.LoggedInContactInfo.LoggedInContact == null)
            {
                return await Task.FromResult(Unauthorized());
            }

            WebPolSourceSiteEffectTerm webPolSourceSiteEffectTerm = new WebPolSourceSiteEffectTerm();

            try
            {
                webPolSourceSiteEffectTerm.PolSourceSiteEffectTermList = await GetPolSourceSiteEffectTermList();

                await DoStore<WebPolSourceSiteEffectTerm>(webPolSourceSiteEffectTerm, $"WebPolSourceSiteEffectTerm.gz");
            }
            catch (Exception ex)
            {
                string inner = ex.InnerException != null ? $"Inner: { ex.InnerException.Message }" : "";
                return await Task.FromResult(BadRequest($"{ ex.Message } { inner }"));
            }

            return await Task.FromResult(Ok(true));
        }
    }
}