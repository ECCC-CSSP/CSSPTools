            this.enums = enums;
            this.CSSPLocalLoggedInService = CSSPLocalLoggedInService;
            this.CSSPLogService = CSSPLogService;
            this.dbLocal = dbLocal;

            CSSPLocalLoggedInService.SetLocalLoggedInContactInfo();

